function isV2Response(dr: V1DocumentResponse | V2DocumentResponse): dr is V2DocumentResponse {
  return "passages" in dr && Array.isArray((dr as V2DocumentResponse).passages);
}

/**
 * Convert any tool result into a VisualScene. Returns null when the tool
 * produced no visual material (e.g. profile_dataset returns text only).
 * The bridge is deliberately defensive — partial/unknown data still
 * produces a usable scene rather than throwing.
 */
function bridgeToolResultToScene(
  toolName: string,
  data: BridgeToolResult,
  file: UploadedFile | null,
  args: Record<string, unknown>
): VisualScene | null {
  const fileName = (args.file_name as string | undefined) ?? file?.name ?? "data";
  const documentId = file?.documentId;
  const docType: DocumentType | "spreadsheet" =
    (file?.extraction?.type as DocumentType | undefined) ??
    ((file?.parsedData?.totalRows ?? 0) > 0 ? "spreadsheet" : "report");

  // 1. Direct scene from compose_visual_scene tool — pass through.
  if (data.scene) return data.scene;

  // 2. Dashboard tool → overview scene.
  if (data.dashboard) {
    return composeScene({
      intent: "overview",
      documentType: "spreadsheet",
      documentId,
      fileName,
      kpis: data.dashboard.kpis.map((k) => ({
        column: k.column,
        label: k.label,
        value: k.value,
        rawValue: k.rawValue,
        isPercent: k.isPercent,
        delta: k.delta,
        deltaPositive: k.deltaPositive,
      })),
      charts: data.dashboard.charts.map(chartConfigToChartProps),
      caption: data.dashboard.subtitle,
      drilldowns: data.dashboard.drilldowns,
    });
  }

  // 3. Chart tool → spotlight chart scene.
  if (data.chart) {
    return composeScene({
      intent: "trend",
      documentType: "spreadsheet",
      documentId,
      fileName,
      charts: [chartConfigToChartProps(data.chart)],
      drilldowns: data.drilldowns,
    });
  }

  // 4. Narrative tool result → scene. Two flavors:
  //    - v2 (query_document_v2): passages-based RAG response.
  //    - v1 (legacy query_document, compose_visual_scene): fact-graph response.
  if (data.documentResponse) {
    const dr = data.documentResponse;

    if (isV2Response(dr)) {
      return composeSceneFromPassages({
        documentId,
        fileName,
        documentType: dr.documentType ?? "report",
        question:
          toolName === "query_document_v2" || toolName === "query_document"
            ? (args.question as string | undefined)
            : undefined,
        answer: dr.answer,
        confidence: dr.confidence,
        passages: dr.passages,
        citedPassageIds: dr.citations.map((c) => c.passageId),
        caveat: dr.caveat,
        drilldowns: data.drilldowns,
        chartable: dr.chartable,
      });
    }

    // v1 path (legacy fact graph)
    const narrativeType = (dr.documentType as DocumentType) ?? docType;
    const intent: SceneIntent = NARRATIVE_TYPE_SET.has(narrativeType as DocumentType)
      ? focusToIntent(dr.focus)
      : "overview";
    return composeScene({
      intent,
      documentType: narrativeType,
      documentId,
      fileName,
      question: toolName === "query_document" ? (args.question as string | undefined) : undefined,
      answer: dr.answer,
      facts: dr.facts,
      metrics: dr.metrics,
      timeline: dr.timeline,
      entities: dr.entities,
      spans: dr.spans ?? {},
      confidence: dr.confidence,
    });
  }

  // No visual material in this tool result.
  return null;
}

/** Safely parse JSON tool args. Returns empty object on failure. */
function safeParseArgs(raw: string, toolName: string): Record<string, unknown> {
  if (!raw || raw.trim() === "") {
    log("tool", `${toolName}: no args provided, using empty object`);
    return {};
  }
  try {
    const parsed = JSON.parse(raw);
    if (typeof parsed !== "object" || parsed === null || Array.isArray(parsed)) {
      logError("tool", `${toolName}: args parsed but not an object:`, parsed);
      return {};
    }
    return parsed as Record<string, unknown>;
  } catch {
    logError("tool", `${toolName}: failed to parse args JSON:`, raw);
    return {};
  }
}

/** Resolve file_name from tool args. Falls back to the most recently uploaded file. */
function resolveFile(
  args: Record<string, unknown>,
  files: UploadedFile[]
): UploadedFile | null {
  const readyFiles = files.filter((f) => f.status === "ready");
  if (readyFiles.length === 0) return null;

  const explicit = args.file_name as string | undefined;
  if (explicit) {
    const match = readyFiles.find(
      (f) => f.name.toLowerCase() === explicit.toLowerCase()
    );
    if (match) return match;
    // Partial match fallback
    const partial = readyFiles.find(
      (f) => f.name.toLowerCase().includes(explicit.toLowerCase())
    );
    if (partial) {
      log("tool", `Resolved "${explicit}" → "${partial.name}" (partial match)`);
      return partial;
    }
    return null;
  }

  // No file_name specified — only default if there's exactly one file (unambiguous)
  if (readyFiles.length === 1) {
    log("tool", `No file_name in args — using only uploaded file "${readyFiles[0].name}"`);
    return readyFiles[0];
  }

  // Multiple files and no explicit name — refuse to guess
  log("tool", `No file_name in args and ${readyFiles.length} files uploaded — cannot determine which file to use`);
  return null;
}

